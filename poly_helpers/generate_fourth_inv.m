function [inv] = generate_fourth_inv(c)

q1=3*power(c(14),2)-8*c(15)*c(13)+2*c(14)*c(12)+3*power(c(12),2)-32*c(11)*c(15)-8*c(13)*c(11);
q2=3*power(c(15),2)+2*c(15)*c(13)+c(14)*c(12)+2*c(15)*c(11)+2*c(13)*c(11)+3*power(c(11),2);
q3=power(c(13),2)-3*c(14)*c(12)+12*c(15)*c(11);
q4=144*c(15)*c(11)*c(1)-36*c(11)*c(10)*c(3)+12*c(11)*power(c(6),2)-36*c(12)*c(14)*c(1)...
    +9*(c(12)*c(9)*c(3)+c(12)*c(10)*c(2))-6*c(12)*c(5)*c(6)+12*power(c(13),2)*c(1)-...
    6*(c(13)*c(8)*c(3)+c(13)*c(9)*c(2))+4*c(13)*c(6)*c(4)+2*c(13)*power(c(5),2)+...
    9*(c(14)*c(7)*c(3)+c(14)*c(8)*c(2))-6*c(14)*c(4)*c(5)-36*c(15)*c(7)*c(2)+...
    12*c(15)*power(c(4),2)-6*c(7)*c(9)*c(6)+9*c(7)*c(10)*c(5)+2*power(c(8),2)*c(6)-...
    c(9)*c(8)*c(5)-6*c(8)*c(10)*c(4)+2*power(c(9),2)*c(4);

inv(1)=q1/q4;
inv(2)=q2/q4;
inv(3)=q3/q4;
end